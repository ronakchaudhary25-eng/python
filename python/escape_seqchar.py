# \n -> New Line
print("Hello\nWorld")
# Output:
# Hello
# World


# \t -> Horizontal Tab
print("Name\tAge")
# Output:
# Name    Age


# \\ -> Backslash
print("C:\\Users\\Ronak\\Desktop")
# Output:
# C:\Users\Ronak\Desktop


# \' -> Single Quote
print('It\'s Python')
# Output:
# It's Python


# \" -> Double Quote
print("He said \"Hello!\"")
# Output:
# He said "Hello!"


# \b -> Backspace
print("ABC\bD")
# Output:
# ABD


# \r -> Carriage Return
print("Hello\rBye")
# Output:
# Byelo


# \f -> Form Feed
print("Hello\fWorld")
# Output:
# Hello World
# (Terminal dependent)


# \v -> Vertical Tab
print("Hello\vWorld")
# Output:
# Hello
#      World
# (Terminal dependent)


# \a -> Alert / Bell
print("Beep!\a")
# Output:
# Beep!
# (May produce a beep sound)


# \0 -> Null Character
print("ABC\0DEF")
# Output:
# ABCDEF
# (Null character is invisible)
